namespace CrmApp.Core.Enums;

// Этапы воронки сделки.
// Порядок имеет значение: используется в DealPipelineService для расчёта конверсии.
public enum DealStage
{
    New = 0,            // Сделка только заведена.
    Qualified = 1,      // Подтверждено что клиент заинтересован.
    Proposal = 2,       // Отправлено КП.
    Negotiation = 3,    // Согласование условий.
    Won = 4,            // Выиграна (терминал).
    Lost = 5,           // Проиграна (терминал).
}
