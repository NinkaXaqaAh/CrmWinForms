namespace CrmApp.Core.Enums;

// Стадия отношений с клиентом — для воронки и фильтров на дашборде.
public enum CustomerStatus
{
    Lead = 0,       // Потенциальный клиент, контакт ещё не подтверждён.
    Active = 1,     // С клиентом ведётся работа.
    Vip = 2,        // Активный с особым приоритетом.
    Inactive = 3,   // Давно нет контактов.
    Blocked = 4,    // Прекращены отношения.
}
